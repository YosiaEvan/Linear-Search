// Program untuk implementasi Bubble Sort sederhana

#include <iostream>
using namespace std;

int main() {
    // Deklarasi array dengan 6 elemen
    int array[] = {77, 42, 35, 12, 101, 5};

    // Menampilkan isi array sebelum diurutkan
    cout << "Elemen sebelum diurutkan:" << endl;
    for (int i = 0; i < 6; i++) {
        cout << array[i] << "\t";
    }

    

    return 0;
}